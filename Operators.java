public class Operators {

    public static void main(String[] args) {
        
        int a = 20;
        int b = 1;

        //Arithmetic Operators
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        //Relational Operators
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a == b);
        System.out.println(a != b);

        
    }

}
