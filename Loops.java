public class Loops {
    
    public static void main(String[] args) {
        
        //while loop
        int count = 5;
        while(count > 0){
            System.out.println("babu bhaiya");
            count--;
        }

        //do while
        do{
            System.out.println("Raju");
            
        }
        while(false);


        //for loop
        int cnt = 5;
        for(int i = 0; i < cnt; i++){
            System.out.println(i);
        }
    }
}
